const int MOD = 1e9 + 7;
const int INF = 1e9;

ostream& operator<<(ostream& out, __int128 x) {
    if (x == 0) return out << "0";
    string s;
    bool neg = false;
    if (x < 0) {
        neg = true;
        x = -x;
    }
    while (x > 0) {
        s += char('0' + x % 10);
        x /= 10;
    }
    if (neg) s += '-';
    reverse(s.begin(), s.end());
    return out << s;
}

__int128 get_sum(ll x) {
    __int128 n = x;
    return n*(n+1)/2;
}

void solve() {
    ll a, b;
    cin >> a >> b;
    if (a > b) swap(a, b);
    __int128 first = get_sum(b);
    __int128 second = get_sum(a - 1);
    cout << (first - second) << '\n';
}

int main() {
    fastio();

    int t = 1;
    cin >> t;
    while (t--) {
        solve();
    }

    return 0;
}